package com.my.db;

public class Informa {
  private String id;
  private String inf_title;
  private String inf_con;
  private String inf_video;

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public String getInf_title() {
    return inf_title;
  }

  public void setInf_title(String inf_title) {
    this.inf_title = inf_title;
  }

  public String getInf_con() {
    return inf_con;
  }

  public void setInf_con(String inf_con) {
    this.inf_con = inf_con;
  }

  public String getInf_video() {
    return inf_video;
  }

  public void setInf_video(String inf_video) {
    this.inf_video = inf_video;
  }
}
