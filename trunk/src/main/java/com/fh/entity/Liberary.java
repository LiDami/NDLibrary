package com.my.db;

public class Liberary {
  private String id;
  private String lib_title;
  private String lib_con;
  private String lib_video;

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public String getLib_title() {
    return lib_title;
  }

  public void setLib_title(String lib_title) {
    this.lib_title = lib_title;
  }

  public String getLib_con() {
    return lib_con;
  }

  public void setLib_con(String lib_con) {
    this.lib_con = lib_con;
  }

  public String getLib_video() {
    return lib_video;
  }

  public void setLib_video(String lib_video) {
    this.lib_video = lib_video;
  }
}
