package com.my.db;

public class Numberres {
  private String num_id;
  private String num_name;
  private String num_con;
  private String num_video;

  public String getNum_id() {
    return num_id;
  }

  public void setNum_id(String num_id) {
    this.num_id = num_id;
  }

  public String getNum_name() {
    return num_name;
  }

  public void setNum_name(String num_name) {
    this.num_name = num_name;
  }

  public String getNum_con() {
    return num_con;
  }

  public void setNum_con(String num_con) {
    this.num_con = num_con;
  }

  public String getNum_video() {
    return num_video;
  }

  public void setNum_video(String num_video) {
    this.num_video = num_video;
  }
}
