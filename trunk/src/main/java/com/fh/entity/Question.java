package  com.fh.entity;


public class Question {
  private String id;
  private String off_title;
  private String off_con;

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public String getOff_title() {
    return off_title;
  }

  public void setOff_title(String off_title) {
    this.off_title = off_title;
  }

  public String getOff_con() {
    return off_con;
  }

  public void setOff_con(String off_con) {
    this.off_con = off_con;
  }
}
