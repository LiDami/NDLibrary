package  com.fh.entity;


public class Reader {
  private String id;
  private String red_title;
  private String red_con;
  private String red_video;

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public String getRed_title() {
    return red_title;
  }

  public void setRed_title(String red_title) {
    this.red_title = red_title;
  }

  public String getRed_con() {
    return red_con;
  }

  public void setRed_con(String red_con) {
    this.red_con = red_con;
  }

  public String getRed_video() {
    return red_video;
  }

  public void setRed_video(String red_video) {
    this.red_video = red_video;
  }
}
